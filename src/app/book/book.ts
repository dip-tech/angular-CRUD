export class Book{
    book_ID:any;
    book_TITLE:any;
    book_AUTHOR:any;
    book_TYPE:any;
    book_PRICE:any;

    constructor(bid:any,btitle:any,bauthor:any,btype:any,bprice:any){
        this.book_ID=bid;
        this.book_TITLE=btitle;
        this.book_AUTHOR=bauthor;
        this.book_TYPE=btype
        this.book_PRICE=bprice;

    }


}